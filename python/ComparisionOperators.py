# Equals To
print(2==2)
print(1==2)
print('hello'=='bye')
print(2.0==2)

# Not Equals To
print(3!=3)
print(4!=5)

# Greater Than(>)
print(2>4)
print(6>3)

# Lesser Than(<)
print(1<0)
print(5<3)

# Greater Than Equal To(>=)
print(6>=4)
print(2>=2)
print(1>=2)

# Lesser Than Equal To(<=)
print(2<=4)
print(2<=2)
print(1<=0)

